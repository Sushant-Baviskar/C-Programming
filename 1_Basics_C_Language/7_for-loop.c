#include<stdio.h>

// used to traverse data structures like arrays,linked lists etc.

int main(){
    int i,j;
    for(i=0,j=0;i<5;i++) // expression 1 initialization of variables is optional!!
                         // expression 2: condition
                         // epression 3: used to update the loop variable.
    {
        printf("%d %d\n",i,j);

    }

    // non zero value = TRUE
    // 0 = FALSE
    
    return 0;
}